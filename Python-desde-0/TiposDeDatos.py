# Imprimir un numero entero.
numero = 50 # variable con un valor de 50.
print(numero) # Se imprime la variable.
print(type(numero)) # Se imprime la variable pero me dice que tipo es, en este caso tipo entero.

# Imprimir un numero booleano.
numero = 50.6 # variable con un valor de 50.6.
print(numero) # Se imprime la variable.
print(type(numero)) # Se imprime la variable pero me dice que tipo es, en este caso tipo float.

